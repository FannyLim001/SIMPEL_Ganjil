package models.pic;

public class ModelKalab {
    int id_kalab;
    String nama_kalab, email_kalab, pass_kalab, kontak_kalab, ruangan_kalab, foto_kalab;

    public int getId_kalab() {
        return id_kalab;
    }

    public void setId_kalab(int id_kalab) {
        this.id_kalab = id_kalab;
    }

    public String getNama_kalab() {
        return nama_kalab;
    }

    public void setNama_kalab(String nama_kalab) {
        this.nama_kalab = nama_kalab;
    }

    public String getEmail_kalab() {
        return email_kalab;
    }

    public void setEmail_kalab(String email_kalab) {
        this.email_kalab = email_kalab;
    }

    public String getPass_kalab() {
        return pass_kalab;
    }

    public void setPass_kalab(String pass_kalab) {
        this.pass_kalab = pass_kalab;
    }

    public String getKontak_kalab() {
        return kontak_kalab;
    }

    public void setKontak_kalab(String kontak_kalab) {
        this.kontak_kalab = kontak_kalab;
    }

    public String getRuangan_kalab() {
        return ruangan_kalab;
    }

    public void setRuangan_kalab(String ruangan_kalab) {
        this.ruangan_kalab = ruangan_kalab;
    }

    public String getFoto_kalab() {
        return foto_kalab;
    }

    public void setFoto_kalab(String foto_kalab) {
        this.foto_kalab = foto_kalab;
    }
}
