package models.pic;

public class ModelPic {
    int id_pic;
    String nama_pic, email_pic, pass_pic, kontak_pic, ruangan_pic, foto_pic;

    public int getId_pic() {
        return id_pic;
    }

    public void setId_pic(int id_pic) {
        this.id_pic = id_pic;
    }

    public String getNama_pic() {
        return nama_pic;
    }

    public void setNama_pic(String nama_pic) {
        this.nama_pic = nama_pic;
    }

    public String getEmail_pic() {
        return email_pic;
    }

    public void setEmail_pic(String email_pic) {
        this.email_pic = email_pic;
    }

    public String getPass_pic() {
        return pass_pic;
    }

    public void setPass_pic(String pass_pic) {
        this.pass_pic = pass_pic;
    }

    public String getKontak_pic() {
        return kontak_pic;
    }

    public void setKontak_pic(String kontak_pic) {
        this.kontak_pic = kontak_pic;
    }

    public String getRuangan_pic() {
        return ruangan_pic;
    }

    public void setRuangan_pic(String ruangan_pic) {
        this.ruangan_pic = ruangan_pic;
    }

    public String getFoto_pic() {
        return foto_pic;
    }

    public void setFoto_pic(String foto_pic) {
        this.foto_pic = foto_pic;
    }
}
